#!/usr/bin/env python3

# print current python version

import sys

print("Python version details:")
print("sys.version={}".format(sys.version))
print("sys.version_info={}".format(sys.version_info))
print("sys.version_info.major={}".format(sys.version_info.major))
print("sys.version_info.minor={}".format(sys.version_info.minor))
print("sys.version_info.micro={}".format(sys.version_info.micro))
print("sys.version_info.releaselevel={}".format(sys.version_info.releaselevel))
print("sys.version_info.serial={}".format(sys.version_info.serial))

